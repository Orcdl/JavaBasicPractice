package Question5;

public class EmployeeMain {
    private String name;
    private int salary;

    public EmployeeMain() {
    }

    public EmployeeMain(String name, int salary) {
        this.name = name;
        this.salary = salary;
    }

    public String getName() {
        return name;
    }

    public int getSalary() {
        return salary;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }
}
